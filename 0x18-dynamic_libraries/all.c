#include <stdio.h>

/**
 * add - The function adds two numbers together
 * @a: First number variable
 * @b: Second number variable
 * Return: addition of both numbers
 */
int add(int a, int b)
{
	return (a + b);
}

/**
 * sub - The function subtrats two numbers
 * @a: First number variable
 * @b: Second number variable
 * Return: subtraction of both numbers
 */
int sub(int a, int b)
{
	return (a - b);
}

/**
 * div - The function adds two numbers together
 * @a: First number variable
 * @b: Second number variable
 * Return: division of both numbers
 */
int div(int a, int b)
{
	return (a / b);
}

/**
 * mul - The function multiplies two numbers together
 * @a: First number variable
 * @b: Second number variable
 * Return: multiplication of both numbers
 */
int mul(int a, int b)
{
	return (a * b);
}

/**
 * mod - The function finds the remainder the first number divided by the second 
 * @a: First number variable
 * @b: Second number variable
 * Return: returns the modulus of both numbers
 */
int mod(int a, int b)
{
	return (a % b);
}
